
namespace Primevil.Game
{
    public struct Size
    {
        public int Width, Height;

        public Size(int width, int height)
        {
            Width = width;
            Height = height;
        }
    }

    public struct SizeF
    {
        public float Width, Height;

        public SizeF(float width, float height)
        {
            Width = width;
            Height = height;
        }
    }
}
